#include <iostream>

int main() {
    int length = 5;
    int width = 3;

    // Calculate area
    int area = length * width;

    // Calculate perimeter
    int perimeter = 2 * (length + width);

    std::cout << "Area: " << area << std::endl;
    std::cout << "Perimeter: " << perimeter << std::endl;

    return 0;
}




